"""
Эмоциональная инерция Алисы.

Каждое сообщение пользователя классифицируется по «тону» (тёплый / нейтральный
/ холодный / агрессивный), это меняет её настроение. Настроение **не сбрасывается
мгновенно** — затухает за несколько сообщений. Так Алиса остаётся обиженной,
если её обидели, или счастливой, если её похвалили.

Реализовано просто на ключевых словах, без LLM-классификатора — это дёшево,
быстро и достаточно для базовой инерции.
"""
from __future__ import annotations

from dataclasses import dataclass


# Чем выше число — тем теплее настроение. Диапазон ~ [-5, +5].
@dataclass(slots=True)
class Mood:
    score: float = 0.0

    @property
    def label(self) -> str:
        s = self.score
        if s >= 3:    return "влюблённое, счастливое"
        if s >= 1.5:  return "тёплое, нежное"
        if s >= 0.3:  return "приподнятое, игривое"
        if s > -0.3:  return "обычное, ровное"
        if s > -1.5:  return "немного задумчивое, прохладное"
        if s > -3:    return "обиженное, отстранённое"
        return "очень обиженное, злое"


_POSITIVE = {
    "люблю", "обожаю", "скучаю", "красивая", "милая", "нежная", "хорошо",
    "круто", "класс", "ты лучшая", "спасибо", "благодарю", "сладкая",
    "🥰", "❤", "❤️", "💕", "😘", "😍",
}
_NEGATIVE = {
    "дура", "бесишь", "отстань", "заткнись", "тупая", "идиотка", "тварь",
    "блять", "сука", "бля", "пошла", "достала", "надоела", "ненавижу",
    "уйди",
}


def update_mood(mood: Mood, user_text: str) -> Mood:
    """Изменяет настроение на основе сообщения + плавное затухание к нулю."""
    text = user_text.lower()
    delta = 0.0

    # Чувствительность к ключевым словам.
    for w in _POSITIVE:
        if w in text:
            delta += 0.8
            break  # один + за сообщение
    for w in _NEGATIVE:
        if w in text:
            delta -= 1.5
            break  # один - за сообщение, и сильнее чем +

    # Плавное затухание: на 15% к 0 за каждое новое сообщение.
    new_score = mood.score * 0.85 + delta
    # Ограничим, чтобы не убегало в бесконечность.
    new_score = max(-5.0, min(5.0, new_score))
    return Mood(score=round(new_score, 2))
